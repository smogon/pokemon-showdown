'use strict';

const fs = require('fs');
const path = require('path');
const { Teams } = require('../dist/sim/teams.js');
const { PRNG } = require('../dist/sim/prng.js');

const FORMAT = 'gen9swserandombattle';
const RUNS = Number(process.argv[2]) || 100000;
const OUT_PATH = path.resolve(__dirname, '../data/random-battles/swse/sets-data.md');

function seedFor() {
	return PRNG.generateSeed();
}

function setKey(set) {
	return [
		set.species,
		set.role || '',
		set.item || '',
		set.ability || '',
		[...set.moves].sort().join('/'),
	].join('\t');
}

function formatMoves(moves) {
	return [...moves].sort().join(', ');
}

const uniqueSets = new Map();
const generationErrors = [];

for (let i = 0; i < RUNS; i++) {
	const seed = seedFor();
	let team;
	try {
		team = Teams.generate(FORMAT, { seed });
	} catch (error) {
		generationErrors.push({ seed, error: error.stack || error.message });
		continue;
	}
	for (const set of team) {
		const key = setKey(set);
		const record = uniqueSets.get(key) || {
			species: set.species,
			role: set.role || '',
			item: set.item || '',
			ability: set.ability || '',
			moves: [...set.moves].sort(),
			count: 0,
		};
		record.count++;
		uniqueSets.set(key, record);
	}
}

const sets = [...uniqueSets.values()].sort((a, b) => (
	a.species.localeCompare(b.species) ||
	a.role.localeCompare(b.role) ||
	a.item.localeCompare(b.item) ||
	a.ability.localeCompare(b.ability) ||
	formatMoves(a.moves).localeCompare(formatMoves(b.moves))
));
const bySpecies = new Map();
for (const set of sets) {
	if (!bySpecies.has(set.species)) bySpecies.set(set.species, []);
	bySpecies.get(set.species).push(set);
}

let output = `# SWSE Random Battle Sets Data\n\n`;
output += `Generated by \`node build full\` using \`Teams.generate('${FORMAT}', { seed })\`.\n\n`;
output += `Teams requested: ${RUNS}\n`;
output += `Teams generated: ${RUNS - generationErrors.length}\n`;
output += `Generation errors: ${generationErrors.length}\n`;
output += `Unique sets: ${sets.length}\n\n`;

if (generationErrors.length) {
	output += `## Generation Errors\n\n`;
	for (const error of generationErrors) {
		output += `- seed ${error.seed.join(',')}: ${error.error.split('\n')[0]}\n`;
	}
	output += `\n`;
}

output += `## Set Variants by Species\n\n`;
for (const [species, speciesSets] of bySpecies) {
	const speciesCount = speciesSets.reduce((total, set) => total + set.count, 0);
	output += `### ${species} (${speciesCount})\n`;
	for (const set of speciesSets) {
		output += `- (${set.count}) ${set.role} @ ${set.item}; ${set.ability}; ${formatMoves(set.moves)}\n`;
	}
	output += `\n`;
}

fs.writeFileSync(OUT_PATH, output);
console.log(`Generated ${path.relative(process.cwd(), OUT_PATH)} from ${RUNS} ${FORMAT} teams.`);
